# estrutura de repetição
# range(start, stop[, step])

# start é o número inicial da sequência (o valor padrão é 0).
# stop é o número final da sequência (o número gerado não inclui esse valor).
# step é o tamanho do intervalo entre os números (o valor padrão é 1).

# Imprime os números de 0 a 9
for n in range(10):
    print(n)

# Imprime os números de 1 a 10
for i in range(1, 11):
    print(i)

# Imprime os números pares de 0 a 9
for i in range(0, 10, 2):
    print(i)

# Imprime os números de 10 a 1 em ordem decrescente
for i in range(10, 0, -1):
    print(i)


